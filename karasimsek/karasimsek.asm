	CLRF	PORTA
	BSF     STATUS,RP0
	CLRF	TRISA
	BCF	STATUS,RP0

	BSF	PORTA,0
SOLA_KAYDIR
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	RLF	PORTA
	BTFSS	PORTA,4			;7. bit 0'sa sola kaydir 1'se saga kaydir
	GOTO    SOLA_KAYDIR
	GOTO	SAGA_KAYDIR

SAGA_KAYDIR	
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	CALL	BEKLE
	RRF	PORTA
	BTFSS	PORTA,0
	GOTO    SAGA_KAYDIR
	GOTO	SOLA_KAYDIR


BEKLE
	MOVLW	H'FF'
	MOVWF	SAYAC
DONGU
	DECFSZ SAYAC
	GOTO   DONGU
